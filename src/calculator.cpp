#include "calculator.h"

double Calculator::add(double a, double b) {
    double result = a + b;
    return result;
}

double Calculator::subtract(double a, double b) {
    double result = a - b;
    return result;
}

double Calculator::multiply(double a, double b) {
    double result = a * b;
    return result;
}

double Calculator::divide(double a, double b) {
    double result = a / b;
    return result;
}
